def needs_image_regeneration(dst_path: Path, src_path: Path) -> bool:
    if FORCE_ALL:
        return True
    if not dst_path.exists():
        return True
    try:
        if src_path.stat().st_mtime > dst_path.stat().st_mtime:
            return True
    except Exception:
        pass
    
    if HAS_PIL and PICTURE_SIZE:
        try:
            with Image.open(dst_path) as img:
                w, h = img.size
                pw, ph = PICTURE_SIZE
                if w > pw or h > ph:
                    return True
        except Exception:
            return True
    return False

