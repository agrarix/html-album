def process_dir(
    src_dir: Path,
    out_dir: Path,
    parent_index: str,
    title: str,
) -> None:
    log_bericht(f"\U0001f4c2  {title}")
    log_bericht(f"    source : {src_dir}")
    log_bericht(f"    output : {out_dir}")

    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / PICTURES_DIR_NAME).mkdir(exist_ok=True)
    (out_dir / THUMBS_DIR_NAME).mkdir(exist_ok=True)

    images = sorted(
        [f for f in src_dir.iterdir() if f.is_file() and f.suffix.lower() in IMAGE_EXTS],
        key=lambda f: f.name.lower(),
        reverse=REVERSE_ORDER,
    )
    log_bericht(f"    photos : {len(images)}")

    # Bereken relatieve pad naar OUTPUT_DIR root voor CSS link
    rel_parts = [p for p in out_dir.relative_to(OUTPUT_DIR).parts if p not in ('.', '/')]
    relative_path_to_root = "../" * len(rel_parts)
    
    version_str = datetime.now().strftime("%Y%m%d%H%M%S")
    index_css_href = f"{relative_path_to_root}html-album.css?v={version_str}"
    slide_css_href = f"../{relative_path_to_root}html-album.css?v={version_str}"

    # Bouw kruimelpad (breadcrumbs)
    rel_path = src_dir.relative_to(SOURCE_DIR)
    root_name = SOURCE_DIR.name
    root_display = f"Photo album : {root_name}"
    
    if rel_path == Path('.'):
        breadcrumb_html = f'<a href="{INDEX_FILE_NAME}">{root_display}</a>'
        slide_breadcrumb_html = f'<a href="../{INDEX_FILE_NAME}">{root_display}</a>'
    else:
        links = []
        slide_links = []
        depth = len(rel_path.parts)
        slide_depth = depth + 1
        
        # Voor indexpagina's
        links.append(f'<a href="{"../" * depth}{INDEX_FILE_NAME}">{root_name}</a>')
        for idx, part_name in enumerate(rel_path.parts[:-1]):
            steps_up = depth - 1 - idx
            links.append(f'<a href="{"../" * steps_up}{INDEX_FILE_NAME}">{part_name}</a>')
        links.append(f'<a href="{INDEX_FILE_NAME}">{rel_path.parts[-1]}</a>')
        breadcrumb_html = " / ".join(links)
        
        # Voor slidepagina's (één niveau dieper wegens slides/ submap)
        slide_links.append(f'<a href="{"../" * slide_depth}{INDEX_FILE_NAME}">{root_name}</a>')
        for idx, part_name in enumerate(rel_path.parts[:-1]):
            steps_up = slide_depth - 1 - idx
            slide_links.append(f'<a href="{"../" * steps_up}{INDEX_FILE_NAME}">{part_name}</a>')
        slide_links.append(f'<a href="../{INDEX_FILE_NAME}">{rel_path.parts[-1]}</a>')
        slide_breadcrumb_html = " / ".join(slide_links)

    # Bepaal target bestandsnamen voor alle afbeeldingen in deze directory
    mapped_images = []
    for img in images:
        target_name = get_new_filename(img)
        target_stem = Path(target_name).stem
        mapped_images.append((img, target_name, target_stem))

    for i, (img, fname, name_no_ext) in enumerate(mapped_images):
        actions     = []

        # Kopieer of hernoem het origineel naar de uitvoermap
        dst_img = out_dir / fname
        if dst_img != img:
            if img.parent == dst_img.parent:
                # Bron- en doelmap zijn hetzelfde: fysiek hernoemen
                if not dst_img.exists():
                    try:
                        img.rename(dst_img)
                        img = dst_img  # Update img naar het nieuwe pad voor thumbnails/exif
                        actions.append("renamed")
                    except Exception as e:
                        log_bericht(f"    ⚠  Error renaming '{img.name}' to '{fname}': {e}")
                else:
                    # Als de hernoemde versie al bestaat, update het pad naar het bestaande bestand
                    img = dst_img
            else:
                # Bron- en doelmap zijn verschillend: kopiëren
                if not dst_img.exists():
                    try:
                        safe_copy(img, dst_img)
                        actions.append("./")
                    except PermissionError as pe:
                        log_bericht(f"    ⚠  Permission denied copying '{fname}': {pe}")
                    except Exception as e:
                        log_bericht(f"    ⚠  Error copying '{fname}': {e}")

        # Genereer verkleinde versie in pictures/ indien PICTURE_SIZE is ingesteld
        if PICTURE_SIZE:
            dst_resized = out_dir / PICTURES_DIR_NAME / fname
            if needs_image_regeneration(dst_resized, img):
                make_resized_image(img, dst_resized)
                actions.append(f"{PICTURES_DIR_NAME}/")

        thumb = out_dir / THUMBS_DIR_NAME / f"{name_no_ext}_thumb.jpg"
        if needs_thumbnail_regeneration(thumb, img):
            make_thumbnail(img, thumb)
            actions.append(f"{THUMBS_DIR_NAME}/")

        prev_slide = mapped_images[i - 1][2] + ".html" if i > 0 else ""
        next_slide = mapped_images[i + 1][2] + ".html" if i < len(mapped_images) - 1 else ""

        generate_slide_html(
            out_dir / PICTURES_DIR_NAME / f"{name_no_ext}.html",
            fname,
            prev_slide,
            next_slide,
            f"../{INDEX_FILE_NAME}",
            slide_breadcrumb_html,
            img,
            slide_css_href,
        )
        
        is_map_foto = " [folder cover]" if i == 0 else ""
        if actions:
            log_bericht(f"    ✓ {fname} ({', '.join(actions)}){is_map_foto}")
        else:
            log_bericht(f"    ✓ {fname} (unchanged){is_map_foto}")

    generate_index_html(
        out_dir / INDEX_FILE_NAME,
        title,
        breadcrumb_html,
        parent_index,
        src_dir,
        out_dir,
        index_css_href,
    )

    subdirs = sorted(
        [d for d in src_dir.iterdir() if d.is_dir() and d.name.lower() not in EXCLUDED],
        key=lambda d: d.name.lower(),
        reverse=REVERSE_ORDER,
    )
    for subdir in subdirs:
        process_dir(
            subdir,
            out_dir / subdir.name,
            f"../{INDEX_FILE_NAME}",
            subdir.name,
        )

