# Genereer index.html voor één map
# ---------------------------------------------------------------------------
def generate_index_html(
    index_file: Path,
    title: str,
    breadcrumb_html: str,
    up_href: str,
    src_dir: Path,
    out_dir: Path,
    css_href: str,
) -> None:
    generated_date = datetime.now().strftime("%d-%m-%Y %H:%M")

    up_btn = ""
    if up_href:
        svg_up = '<svg width="1em" height="1em" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="3.5" stroke-linecap="round" stroke-linejoin="round" style="display: inline-block; vertical-align: middle;"><line x1="12" y1="19" x2="12" y2="5"></line><polyline points="5 12 12 5 19 12"></polyline></svg>'
        up_btn = f'<a href="{up_href}" class="nav-btn up-btn" title="Up to parent directory">{svg_up}</a>'

    images = sorted(
        [f for f in src_dir.iterdir() if f.is_file() and f.suffix.lower() in IMAGE_EXTS],
        key=lambda f: f.name.lower(),
        reverse=REVERSE_ORDER,
    )

    img_cells: list[str] = []
    for i, img in enumerate(images):
        fname       = get_new_filename(img)
        name_no_ext = Path(fname).stem
        thumb_rel   = f"{THUMBS_DIR_NAME}/{name_no_ext}_thumb.jpg"
        slide_rel   = f"{PICTURES_DIR_NAME}/{name_no_ext}.html"
        
        is_map_foto = (i == 0)
        cell_class = "thumb-cell map-foto" if is_map_foto else "thumb-cell"
        title_text = f"{fname} [folder cover]" if is_map_foto else fname
        label_text = f"\U0001f4c1 {fname}" if is_map_foto else fname
        
        img_cells.append(
            f'        <div class="{cell_class}">\n'
            f'            <a href="{slide_rel}" title="{title_text}">\n'
            f'                <div class="thumb-img-wrap">\n'
            f'                    <img src="{thumb_rel}" alt="{fname}" loading="lazy">\n'
            f'                </div>\n'
            f'                <div class="thumb-label">{label_text}</div>\n'
            f'            </a>\n'
            f'        </div>'
        )

    subdirs = sorted(
        [d for d in src_dir.iterdir() if d.is_dir() and d.name.lower() not in EXCLUDED],
        key=lambda d: d.name.lower(),
        reverse=REVERSE_ORDER,
    )

    dir_cells: list[str] = []
    for subdir in subdirs:
        dname            = subdir.name

        first_img = next(
            (f for f in sorted(subdir.iterdir(), key=lambda x: x.name.lower(), reverse=REVERSE_ORDER) if f.is_file() and f.suffix.lower() in IMAGE_EXTS),
            None,
        )

        if first_img:
            first_img_stem = Path(get_new_filename(first_img)).stem
            folder_thumb_dst = out_dir / THUMBS_DIR_NAME / f"folder_{dname}_{first_img_stem}_thumb.jpg"
            if needs_thumbnail_regeneration(folder_thumb_dst, first_img):
                make_thumbnail(first_img, folder_thumb_dst)
                log_bericht(f"    ✓ Folder '{dname}' ({THUMBS_DIR_NAME}/ with photo: {first_img.name})")
            else:
                log_bericht(f"    ✓ Folder '{dname}' (unchanged, photo: {first_img.name})")
            thumb_tag = f'<img src="{THUMBS_DIR_NAME}/folder_{dname}_{first_img_stem}_thumb.jpg" alt="{dname}" loading="lazy">'
            label = f"\U0001f4c1 {dname}"
        else:
            thumb_tag = '<div class="folder-icon">\U0001f4c1</div>'
            label     = dname

        dir_cells.append(
            f'        <div class="thumb-cell folder">\n'
            f'            <a href="{dname}/{INDEX_FILE_NAME}" title="{dname}">\n'
            f'                <div class="thumb-img-wrap">\n'
            f'                    {thumb_tag}\n'
            f'                </div>\n'
            f'                <div class="thumb-label">{label}</div>\n'
            f'            </a>\n'
            f'        </div>'
        )

    all_cells = "\n".join(img_cells + dir_cells)

    # Formateer datum en tijd
    now = datetime.now()
    date_str = now.strftime("%d-%m-%Y")
    time_str = now.strftime("%H:%M")

    footer_tmpl = cfg.get("FOOTER", "Generated by (Agrarix)${PGM} v${VER} ${DATE} ${TIME}")
    footer_text = footer_tmpl
    footer_text = footer_text.replace("${PGM}", PGM).replace("{PGM}", PGM)
    footer_text = footer_text.replace("${VER}", VERSION).replace("{VER}", VERSION).replace("{VERSION}", VERSION)
    footer_text = footer_text.replace("${DATE}", date_str).replace("{date_str}", date_str)
    footer_text = footer_text.replace("${TIME}", time_str).replace("{time_str}", time_str)

    html = f"""\
<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="UTF-8">
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <meta name="keywords" content="photoalbum,gallery,photo,online">
    <title>{title}</title>
    <link rel="stylesheet" href="{css_href}">
</head>
<body>
<div class="page-wrap">
    <div class="album-header">
        {up_btn}
        <span class="header-title">{breadcrumb_html}</span>
    </div>
    <div class="thumb-grid">
{all_cells}
    </div>
    <div class="footer">{footer_text}</div>
</div>
</body>
</html>
"""
    index_file.write_text(html, encoding="utf-8")

