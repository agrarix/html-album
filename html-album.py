def get_css() -> str:
    tw, th = THUMB_SIZE
    cell_w = tw + 10
    
    cols = int(cfg.get("COLUMNS", 0))
    if cols > 0:
        page_max_w = max(880, cell_w * cols + 40)
    else:
        page_max_w = max(880, cell_w * 2 + 40)
    
    css_path = SCRIPT_DIR / "html-album.css"
    if css_path.exists():
        css_res = css_path.read_text(encoding="utf-8")
    else:
        log_bericht("    ⚠ Warning: html-album.css not found in script directory! Minimal fallback styling will be used.")
        if sys.stdin.isatty():
            try:
                antwoord = input("    Press [Enter] to continue, or 'q' to stop: ").strip().lower()
                if antwoord == 'q':
                    log_bericht("    Generation aborted by user.")
                    sys.exit(0)
            except (EOFError, KeyboardInterrupt):
                pass
        css_res = "* { box-sizing: border-box; }"
    
    css_res = css_res.replace("max-width: 880px;", f"max-width: {page_max_w}px;")
    css_res = css_res.replace("width: 148px;", f"width: {cell_w}px;")
    css_res = css_res.replace("width: 138px;", f"width: {tw}px;")
    css_res = css_res.replace("height: 112px;", f"height: {th}px;")
    css_res = css_res.replace("max-width: 138px;", f"max-width: {tw}px;")
    css_res = css_res.replace("max-height: 112px;", f"max-height: {th}px;")
    css_res = css_res.replace("line-height: 112px;", f"line-height: {th}px;")
    
    if cols > 0:
        css_res += f"\n@media (max-width: 600px) {{\n    .thumb-grid {{\n        display: grid;\n        grid-template-columns: repeat({cols}, {cell_w}px);\n    }}\n}}\n"
    
    if th > 140:
        folder_font_size = min(120, int(th * 0.5))
        css_res = css_res.replace("font-size: 64px;", f"font-size: {folder_font_size}px;")
        
    return css_res

